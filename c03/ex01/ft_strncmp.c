int ft_strncmp(char *s1, char *s2, unsigned int n)
{
	while (*s1 != '\0' || *s2 != '\0'&& n != 0)
	{	
		*s1++;
		*s2++;
		if (*s1 == *s2)
		{
			continue ;
		}
		else if (*s1 > *s2)
		{
			return (1);
		}
		else if (*s1 < *s2)
		{
			return (-1);
		}
		n--;
	}
	return (0);
}
