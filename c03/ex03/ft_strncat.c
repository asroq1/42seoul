char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int	i;
	unsigned int	j;

	j = 0;
	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (nb != 0)
	{
		dest[i] = src[j];
		i++;
		j++;
		nb--;
	}
	return (dest);
}
