int	ft_find_next_prime(int nb)
{
	long long	i;

	i = 2;
	if (nb <= 2)
	{
		return (2);
	}
	while (i * i <= nb)
	{
		if (nb % i == 0)
		{
			nb += 1;
			i = 1;
		}
		i++;
	}
	return (nb);
}
